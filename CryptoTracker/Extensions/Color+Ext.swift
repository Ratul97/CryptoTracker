import SwiftUI

extension Color {
    static let theme = ColorTheme()
}

struct ColorTheme {
    let accent = Color(.accent)
    let background = Color(.background)
    let tagBackground = Color(.tagBackground)
    let tagText = Color(.tagText)
    let red = Color(.red)
    let secondaryText = Color(.secondaryText)
}
